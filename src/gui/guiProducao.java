    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        
        cboCostura.removeAllItems();
        for (Costura c: dao.daoCostura.getCosturas()) {
            cboCostura.addItem(c);
        }
        
        cboMaquina.removeAllItems();
        for (Maquina m: daoMaquina.getMaquinas()) {
            cboMaquina.addItem(m);
        }
        
        cboPeca.removeAllItems();
        for (Peca p: daoPeca.getPecas()) {
            cboPeca.addItem(p);
        }
        
        
        lstProducao.setListData(daoProducao.getProducoes().toArray());
    }//GEN-LAST:event_formWindowActivated

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        cboCostura.removeAllItems();
        for (Costura c: dao.daoCostura.getCosturas()) {
            cboCostura.addItem(c);
        }
        
        cboMaquina.removeAllItems();
        for (Maquina m: daoMaquina.getMaquinas()) {
            cboMaquina.addItem(m);
        }
        
        cboPeca.removeAllItems();
        for (Peca p: daoPeca.getPecas()) {
            cboPeca.addItem(p);
        }
        
        
        lstProducao.setListData(daoProducao.getProducoes().toArray());
    }//GEN-LAST:event_formWindowOpened

