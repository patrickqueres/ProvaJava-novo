    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Producao p = (Producao) lstProducao.getSelectedValue();

        dao.daoProducao.remover(p);

        lstProducao.setListData(daoProducao.getProducoes().toArray());
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        String maquina = (String) cboMaquina.getSelectedItem().toString();
        String costureira = (String) cboCostura.getSelectedItem().toString();
        String peca = (String) cboPeca.getSelectedItem().toString();
        Producao p = new Producao(maquina, costureira, peca);

        txtDataInicio.setValue(p.getData().getTime());
        
        daoProducao.adicionar(p);

        lstProducao.setListData(daoProducao.getProducoes().toArray());
    }//GEN-LAST:event_btnAdicionarActionPerformed

