    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Costureira c = (Costureira) lstCostureira.getSelectedValue();

        dao.daoFuncionario.remover(c);

        lstCostureira.setListData(daoFuncionario.getCostureiras().toArray());
                
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String matricula = txtMatricula.getText();
        Double salario = Double.parseDouble(txtSalarioMin.getText());
        //Double salario = txtSalarioMin.getText();
        Costureira c = new Costureira(nome, matricula, salario);
        daoFuncionario.adicionar(c);
        txtNome.setText("");
        txtMatricula.setText("");
        txtSalarioMin.setText("");
        lstCostureira.setListData(daoFuncionario.getCostureiras().toArray());
    }//GEN-LAST:event_btnAdicionarActionPerformed

