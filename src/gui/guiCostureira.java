    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        cboCostu.removeAllItems();
        for (Costura c: dao.daoCostura.getCosturas()) {
            cboCostu.addItem(c);
        }
        
        cboSuper.removeAllItems();
        for (Supervisor c: dao.daoFuncionario.getSupervisores()) {
            cboSuper.addItem(c);
        }
        
        lstCostureira.setListData(daoFuncionario.getCostureiras().toArray());
        
        
    }//GEN-LAST:event_formWindowActivated

