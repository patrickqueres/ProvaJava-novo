    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        cboMaquina.removeAllItems();
        for (Maquina m: daoMaquina.getMaquinas()) {
            cboMaquina.addItem(m);
        }
        
        cboSupervisor.removeAllItems();
        for (Supervisor s: dao.daoFuncionario.getSupervisores()) {
            cboSupervisor.addItem(s);
            
        lstManutencao.setListData(daoManutencao.getManutencoes().toArray());
    }//GEN-LAST:event_formWindowActivated
    }
    
