    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Manutencao m = (Manutencao) lstManutencao.getSelectedValue();

        dao.daoManutencao.remover(m);

        lstManutencao.setListData(daoManutencao.getManutencoes().toArray());
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        String maquina = (String) cboMaquina.getSelectedItem().toString();
        String supervisor = (String) cboSupervisor.getSelectedItem().toString();
        Manutencao m = new Manutencao(maquina, supervisor);
                
        txtDataInicio.setValue(m.getData().getTime());
        txtDataFim.setValue(m.getDatafim().getTime());        
        daoManutencao.adicionar(m);
        
        lstManutencao.setListData(daoManutencao.getManutencoes().toArray());
    }//GEN-LAST:event_btnAdicionarActionPerformed

